    boolean operatorJustPressed = false;
    private void button(String number) {
        // Clear after AC, =, or %
        if (jTextField_Display2.getText().equals("AC") ||
            jTextField_Display3.getText().equals("=") ||
            jTextField_Display4.getText().equals("%")) {

            jTextField_Display2.setText("");
            jTextField_Display3.setText("");
            jTextField_Display4.setText("");
        }

        // If operator was just pressed, now we start inputting num2
        if (operatorJustPressed) {
            operatorJustPressed = false; // reset flag
            jTextField_Display.setText(jTextField_Display.getText() + number); // allow num2 to continue right after operator
        } else {
            jTextField_Display.setText(jTextField_Display.getText() + number); // normal case
        }
    }
    
    private String format(double result){
        if (result == (int) result){
            return String.valueOf((int) result);
        }
        else{
            DecimalFormat df = new DecimalFormat("#.##");
            return df.format(result); 
        }
    }
    
    

