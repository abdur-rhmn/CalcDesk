    private void jButton_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_equalActionPerformed
        jTextField_Display3.setText("=");
        jTextField_Display4.setText(jTextField_Display.getText());
        
        
        String expression = jTextField_Display.getText(); // e.g. "5+3"
        
        // Replace symbols with real operators
        expression = expression.replace("×", "*");
        expression = expression.replace("÷", "/");
        
        int opIndex = expression.indexOf(operator);       // find position of operator
        
        if (opIndex == -1) {
        jTextField_Display.setText("Invalid");
        return;
        }
        
        // Extract num1 from start to operator index
        num1 = Double.parseDouble(expression.substring(0, opIndex));

        // Extract num2 from after operator till end
        num2 = Double.parseDouble(expression.substring(opIndex + 1));

        result = 0;
        switch (operator) {
            case "+":
                result = num1 + num2;
                break;
            case "-":
                result = num1 - num2;
                break;
            case "*":
                result = num1 * num2;
                break;
            case "/":
                if (num2 == 0) {
                    jTextField_Display.setText("Error");
                    return;
                }
                result = num1 / num2;
                break;
            case "^":
                result = Math.pow(num1, num2);
                break;
            default:
                jTextField_Display.setText("Error");
                return;
        }

        jTextField_Display.setText(format(result));

    }//GEN-LAST:event_jButton_equalActionPerformed

